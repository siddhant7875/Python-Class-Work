#wap check if element exists in list in python

# Define the list
my_list = [10, 20, 30, 40, 50]

# Define the element to check
element_to_check = 30

# Check if the element exists in the list using the 'in' keyword
if element_to_check in my_list:
    print(f"The element {element_to_check} exists in the list.")
else:
    print(f"The element {element_to_check} does not exist in the list.")
